public class tablaverdad {
    public static void main(String[] args) {
        boolean p , q;
        p= true;
        q= false;
        System.out.println("p = "+p);
        System.out.println("p ="+q);
        System.out.println("p && q ="+(p && q ));
        System.out.println("p || q ="+(p || q ));
    
    }
}
